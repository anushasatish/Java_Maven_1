public class SwapNumber
    {
        public static void main(String[] args) {
            /* Initialising Numbers*/
            int i = 50;
            int j = 100;
            int s;
            System.out.println("Before Swapping numbers are : "+i +" " +j);

            /* Swapping Numbers*/
            s =i;
            i =j;
            j=s;

            System.out.println("After Swapping numbers are : "+i +" " +j);


        }
    }

